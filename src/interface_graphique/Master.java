    // Les pronostics des joueurs ! 
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        int nombreDePronostic = 1;
        int tailleTableau = 40 * GestionExcel.NOMBRE_DE_JOUEUR;
        pronosticsJoueurs = new String[tailleTableau];
        String pronosticPath;
        GestionExcel excel = new GestionExcel();
        
        pronosticPath = excel.open();
        pronosticsJoueursTemp = excel.lireProno(pronosticPath,nombreDePronostic);
        
        int j;
        
        for(int i = 0; i < 40; i++){
           j= i + i*nbreDeFichier;
            pronosticsJoueurs[j] = pronosticsJoueursTemp[i];
        }
        
        nbreDeFichier++;  
        
        String pronosticPathSave = "Vos_Pronostics.csv";
        int s;
        s = excel.saveAs(pronosticsJoueurs, pronosticPathSave);
        
        if (s == 1) {
            JOptionPane.showMessageDialog(null, "sauvegarde réussie", "Information", INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed
    
