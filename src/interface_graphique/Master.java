    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        String[] match = new String[20+GestionExcel.NOMBRE_DE_JOUEUR*2];
        String[] classement = new String[GestionExcel.NOMBRE_DE_JOUEUR*2];
        GestionExcel test = new GestionExcel();
        int k = 0; 
        int s; 
        
        // Remplissage du tableau de matchs
        
        for (int i = 0; i < 10; i++) {
            for (int j = 0; j < 2; j++) {
                match[k] = jTable1.getValueAt(i, j).toString(); 
                
                   if(jTable1.getValueAt(i, j)==null){
                   JOptionPane.showMessageDialog(null, "Veuillez remplir toutes les cases", "Attention",WARNING_MESSAGE); 
                }
                k++;
            }
        }
        
        // Remplissage du tableau de classement
        k=0;
        for (int i = 0; i <GestionExcel.NOMBRE_DE_JOUEUR; i++) {
            for (int j = 0; j < 2; j++) {
                classement[k] = jTable2.getValueAt(i, j).toString(); 
                
                   if(jTable2.getValueAt(i, j)==null){
                   JOptionPane.showMessageDialog(null, "Veuillez ouvrir le classement avant", "Attention",WARNING_MESSAGE); 
                }
                k++;
            }
        }
        
        for(int i = 0; i<(GestionExcel.NOMBRE_DE_JOUEUR*2); i++){
            match[20+i] = classement[i];
            }
        
        String path = "Match_sauvegarde.csv";
        s=test.saveAs(match,path);
        
        if(s==1){
        JOptionPane.showMessageDialog(null, "sauvegarde réussie", "Information",INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

